<h4>1. <u>ரகசிய தகவல்</u></h4>
<p><u>அ. நிறுவனத்தின் தகவல்</u> : எனது வேலைவாய்ப்பு காலத்திலும், அதன்பிறகு மூன்று வருட காலத்திலும், நிறுவனத்தின் நன்மை தவிர, அல்லது எந்தவொரு நபருக்கும், நிறுவனத்துக்கும் அல்லது நிறுவனத்தின் எழுத்துப்பூர்வ அங்கீகாரமின்றி இருக்கும் நிறுவனத்துக்கும், நிறுவனத்தின் எந்த ரகசிய தகவலும் நம்பிக்கையுடன் இருப்பதற்கும், எல்லா நேரங்களிலும் நம்பிக்கையுடன் இருக்க</p>

<p>நான் ஒப்புக்கொள்கிறேன்.எனக்கு புரிவது என்னவென்றால் “ரகசிய தகவல்” என்பது எந்தவொரு நிறுவனத்தின் தனியுரிம தகவல், தொழில்நுட்ப தரவு, வர்த்தக ரகசியங்கள் அல்லது ஆராய்ச்சி, தயாரிப்புத் திட்டங்கள், தயாரிப்புகள், சேவைகள், வாடிக்கையாளர் பட்டியல்கள், சந்தைகள், மென்பொருள், முன்னேற்றங்கள், கண்டுபிடிப்புகள் உள்ளிட்டவற்றை உள்ளடக்கியது, ஆனால் அவை மட்டுமல்ல. செயல்முறைகள், சூத்திரங்கள், தொழில்நுட்பம், வடிவமைப்புகள், வரைபடங்கள், பொறியியல், வன்பொருள் உள்ளமைவு தகவல், சந்தைப்படுத்தல், நிதி அல்லது நிறுவனம் எனக்கு நேரடியாகவோ அல்லது மறைமுகமாகவோ வெளிப்படுத்திய பிற வணிகத் தகவல்கள்.<p>

<p><u>ஆ. விதிவிலக்குகள்</u> : நான் நிரூபிக்கக்கூடிய ரகசிய தகவலின் பகுதிக்கு மேற்கூறிய கடமைகள் மற்றும் கட்டுப்பாடுகள் பொருந்தாது :</p>
<ul><li>கிடைத்தது அல்லது பொதுவாக பொதுமக்களுக்கு கிடைத்தது அல்லது</li>
    <li>கோரப்பட்டது அல்லது சட்டப்பூர்வமாக கட்டாயப்படுத்தப்பட்டது (வாய்வழி கேள்விகள், விசாரணையாளர்கள், தகவல் அல்லது ஆவணங்களுக்கான கோரிக்கைகள், சப்போனா, சிவில் அல்லது கிரிமினல் புலனாய்வு கோரிக்கை அல்லது ஒத்த செயல்முறை) அல்லது தடைசெய்யப்பட்ட எந்தவொரு வெளிப்பாட்டையும் செய்ய ஒரு ஒழுங்குமுறை அமைப்பு தேவைப்படுகிறது இல்லையெனில், இந்த ஒப்பந்தத்தால் கட்டுப்படுத்தப்பட்டால், நான் கட்டுப்படுவேன்.</li>
    <li>அத்தகைய எந்தவொரு கோரிக்கையையும் (களை) உடனடியாக அறிவித்து நிறுவனத்திற்கு வழங்குவதன் மூலம் நிறுவனம் பொருத்தமான பாதுகாப்பு உத்தரவு அல்லது பிற பொருத்தமான தீர்வுகளைத் தேடலாம் மற்றும்</li>

