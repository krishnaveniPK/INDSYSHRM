</ul>
<p><u>இ. முன்னாள் வேலைவாய்ப்பு தகவல்</u> : நிறுவனத்துடனான எனது வேலையின் போது நான் ஒப்புக்கொள்வது என்னவென்றால், முறையற்ற முறையில் பயன்படுத்தக்கூடாதுஅல்லது எந்த தனியுரிம தகவலையும் வெளியிடவும் அல்லது எந்தவொரு முன்னாள் அல்லது ஒரே நேரத்தில் முதலாளியின் வர்த்தக இரகசியங்கள் அல்லது மற்ற நபர் அல்லது எந்தவொரு வெளியிடப்படாத ஆவணத்தையும் நிறுவனத்தின் வளாகத்திற்கு கொண்டு வரக்கூடாது அல்லது அத்தகைய எந்தவொரு முதலாளிக்கும் சொந்தமான தனியுரிம தகவல் அல்லது அத்தகைய முதலாளி, நபர் அல்லது நபர் அல்லது நிறுவனம் எழுத்துப்பூர்வமாக ஒப்புக் கொள்ளாவிட்டால்.</p>

<p><u>ஈ. மூன்றாம் தரப்பு தகவல்</u> : எதிர்காலத்தில் மூன்றாம் தரப்பினரிடமிருந்து அவர்களின் ரகசிய அல்லது தனியுரிம தகவல்களை சில வரையறுக்கப்பட்ட நோக்கங்களுக்காக மட்டுமே பயன்படுத்துவேன் என்பதை நான் உணர்கிறேன். இதுபோன்ற அனைத்து ரகசிய அல்லது தனியுரிம தகவல்களையும் கடுமையான நம்பிக்கையுடன் வைத்திருக்க ஒப்புக்கொள்கிறேன், அதை எந்தவொரு நபருக்கும், நிறுவனத்திற்கும் அல்லது நிறுவனத்திற்கும் வெளியிடக்கூடாது அல்லது அத்தகைய மூன்றாம் தரப்பினருடனான நிறுவனத்தின் உடன்படிக்கைக்கு இணங்க நிறுவனத்திற்காக எனது பணிகளை மேற்கொள்வதில் தேவையானதைத் தவிர அதைப் பயன்படுத்தக்கூடாது.</p>

<p><u>2. நிர்வாக பொருட்களை  திரும்ப பெறுதல்</u> : எனது வேலையில் இருந்து விலகும் போது,  நிறுவனத்தின் வணிகம் தொடர்பான அனைத்து ஆவணங்களும், அறிக்கைகள், சுருக்கங்கள், பட்டியல்கள், கடிதப் போக்குவரத்து, தகவல், கணினி கோப்புகள், கணினி வட்டுகள் மற்றும் நிறுவனத்துடன் எனது வேலையின் போது நான் பெற்ற மற்ற எல்லா பொருட்களும் மற்றும் அத்தகைய பொருட்களின் அனைத்து நகல்களும் திரும்ப ஒப்படைத்துவிடுவேன்.</p>

<p><u>3. சட்டபூர்வமான மற்றும் தகுதியான தீர்வுகள்</u> : இந்த ஒப்பந்தத்தின் எந்தவொரு விதிகளை மீறினாலும் தடை உத்தரவைப் பெற நிறுவனத்திற்கு உரிமை உண்டு என்பதையும் நான் உணர்கிறேன்.அத்தகைய போட்டி அல்லது வெளிப்பாட்டைத் தடுக்க ஒரு தடை உத்தரவு, குறிப்பிட்ட செயல்திறன் அல்லது பிற சமமான தீர்வைப் பெற நிறுவனத்திற்கு உரிமை உண்டு என்பதையும், மற்ற சட்ட தீர்வுகளுக்கு நிறுவனத்திற்கு உரிமை வழங்கலாம் என்பதையும் நான் உணர்கிறேன். வழக்கறிஞரின் கட்டணம் மற்றும் செலவுகள் உட்பட.</p>

