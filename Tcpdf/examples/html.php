<p>நல்ல ஆலோசனையுடனும், சைன்மார்க்ஸ் இண்டஸ்ட்ரீஸ் இந்தியா பிரைவேட் லிமிடெட் (கம்பெனி) எனது வேலைவாய்ப்பு அல்லது தொடர்ச்சியான வேலைவாய்ப்பைக் கருத்தில் கொண்டு, அடியிற் கையொப்பமிட்ட</p>

